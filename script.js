// Counter animation function
function animateCounters() {
    const counters = document.querySelectorAll('.cert-item');
    
    counters.forEach((counter, index) => {
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    // Add a subtle animation when certification comes into view
                    setTimeout(() => {
                        entry.target.style.transform = 'translateX(0) scale(1.05)';
                        setTimeout(() => {
                            entry.target.style.transform = 'translateX(0) scale(1)';
                        }, 200);
                    }, index * 200);
                    observer.unobserve(entry.target);
                }
            });
        });
        
        observer.observe(counter);
    });
}

// Smooth reveal animation for sections
function revealOnScroll() {
    const sections = document.querySelectorAll('section');
    
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.offsetHeight;
        const windowHeight = window.innerHeight;
        const scrollY = window.scrollY;
        
        if (scrollY + windowHeight > sectionTop + sectionHeight * 0.3) {
            section.style.opacity = '1';
            section.style.transform = 'translateY(0)';
        }
    });
}

// Initialize reveal on scroll
window.addEventListener('scroll', revealOnScroll);
window.addEventListener('load', revealOnScroll);

// Add loading animation
window.addEventListener('load', function() {
    document.body.style.opacity = '0';
    document.body.style.transition = 'opacity 0.5s ease';
    
    setTimeout(() => {
        document.body.style.opacity = '1';
    }, 100);
});

// Add cursor trail effect
let mouseX = 0;
let mouseY = 0;
let cursorTrail = [];

document.addEventListener('mousemove', function(e) {
    mouseX = e.clientX;
    mouseY = e.clientY;
    
    // Create trail effect
    if (cursorTrail.length > 10) {
        cursorTrail.shift();
    }
    
    cursorTrail.push({
        x: mouseX,
        y: mouseY,
        timestamp: Date.now()
    });
});

// Create cursor trail elements
for (let i = 0; i < 5; i++) {
    const trail = document.createElement('div');
    trail.className = 'cursor-trail';
    trail.style.cssText = `
        position: fixed;
        width: 6px;
        height: 6px;
        background: linear-gradient(45deg, #3498db, #2ecc71);
        border-radius: 50%;
        pointer-events: none;
        z-index: 9999;
        opacity: ${0.3 - i * 0.05};
        transform: scale(${1 - i * 0.1});
    `;
    document.body.appendChild(trail);
}

// Update cursor trail positions
function updateCursorTrail() {
    const trails = document.querySelectorAll('.cursor-trail');
    
    trails.forEach((trail, index) => {
        if (cursorTrail[cursorTrail.length - 1 - index]) {
            const pos = cursorTrail[cursorTrail.length - 1 - index];
            trail.style.left = pos.x + 'px';
            trail.style.top = pos.y + 'px';
        }
    });
    
    requestAnimationFrame(updateCursorTrail);
}

updateCursorTrail();

